int binPow(int a, int b){
	int ans = 1;
	while(b > 0){
		if (b & 1) ans = 1ll * ans * a % MOD;
		a = 1ll * a * a % MOD; b >>= 1;
	}
	return ans;
}

int mul(int a, int b){
	int ans = 1;
	while(b > 0){
		if (b & 1) ans = 1ll * (ans + a) % MOD;
		a = 2ll * a % MOD; b >>= 1;
	}
	return ans;
}

